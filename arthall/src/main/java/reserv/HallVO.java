package reserv;

public class HallVO {
	
	private int no;
	private String hallName;
	private int seatA;
	private int seatB;
	private int seatC;
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getHallName() {
		return hallName;
	}
	public void setHallName(String hallName) {
		this.hallName = hallName;
	}
	public int getSeatA() {
		return seatA;
	}
	public void setSeatA(int seatA) {
		this.seatA = seatA;
	}
	public int getSeatB() {
		return seatB;
	}
	public void setSeatB(int seatB) {
		this.seatB = seatB;
	}
	public int getSeatC() {
		return seatC;
	}
	public void setSeatC(int seatC) {
		this.seatC = seatC;
	}

	

}
